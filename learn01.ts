// 1.Angular介绍、安装Angular Cli、创建运行Angular项目

/* 
    一、Angular介绍

        Angular是一款来自谷歌的开源的web前端框架，诞生于2009年，有Misko Hevery等人创建，
        后为Google所收购。是一款优秀的前端js框架，已经被用于Google多款产品当中。

        根据项目数统计Angular（1.x/2.x/4.x/5.x/6.x/7.x)是现在网上使用量最大的框架。

        Angular基于TypeScript和react、Vue相比Angular更适适合中大型企业项目。

        目前2018年11月25日Angular最新版本Angular7.x。根据官方介绍，Angular每过几个月就会更新一个版本。
        此教程适用于后期更新的Angular8.x、Angular9.x。。。；
*/

/*
    二、学习Angular必备基础

    必备基础：html、css、js、es6

    如果有Typscript基础更容易理解，没有TypeScript基础也可以学习此教程，此教程中用到的TypeScript语法会详细讲解
*/

/* 
    三、Angular环境搭建

    1.安装前准备工作：
        1.1、安装nodejs

            安装Angular的计算机上必须安装最新的nodejs--注意安装nodejs稳定版；
        
        1.2、安装cnpm

            npm可能安装失败建议先用npm安装一下cnpm用淘宝镜像安装

        npm install -g cnpm --registry=https://registry.npm.taobao.org
    
    2.使用npm/cnpm命令安装angular/cli（只需要安装一次）

        npm install -g @angular/cli

*/

/* 
    四、Angular创建项目

    1.打开命令行工具找到你要创建的目录

    2.创建项目

        直接安装：
        ng new 项目名

        如果要跳过npm i安装：
        ng new 项目名 --skip-install
    
    
*/

